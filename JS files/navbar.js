$(document).ready(
			function(){
				$("#login").mouseover(function()
				{
  					$("#login").css("background-color", "#FFFFFF");
  					$("#login").css("color", "#000000");
				});
				$("#login").mouseout(function()
				{
  					$("#login").css("background-color", "#2D4059");
  					$("#login").css("color", "#FFB400");
				});
			}
			);