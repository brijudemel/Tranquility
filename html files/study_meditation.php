	<h1>Finding your focus: meditations for exams and presentations</h1>
	<h4>When the stakes are high, the emotions usually match. And with emotions swirling, it’s not always easy to sit down and focus — fear of failure, test anxiety, procrastination, or busy schedules can get in the way of dedicated study or prep time.</h4>
	<p style="font-size: 1.4rem;">We’re approaching that time of year again when college students in America prepare for their looming midterms and upcoming finals. But it’s not only exam season that brings do-or-die pressure; it’s studies of any kind, for, say, an essay, an education course, a certification, a project, or a big presentation.<br>With so much going on, finding and keeping our focus can be challenging in an increasingly busy, tech-connected world. But if we can be calm and clear-headed, it’s easier to find that sweet spot of relaxed focus that’s key to us learning and retaining new information.<br>This is where training the mind comes in useful. It’s through meditation that we improve our ability to place our attention on one thing for a fixed amount of time, without getting distracted. The more we build our awareness, the more sustainable our focus becomes.</p><hr>
	<h2>Focus, focus, focus … </h2>
	<p style="font-size: 1.4rem;">Think of this month’s featured collection as your very own study buddy. It’s full of recommended meditations and exercises to help you knuckle down, find that focus, and nail whatever test or project is looming on the horizon. You got this. We got you.</p>
	<h3>SITTING DOWN TO FOCUS</h3>
	<p style="font-size: 1.4rem;">Focus is innate; without effort. But we need to be in the right frame of mind, and create the right environment, before dropping into that focus. These exercises are designed to set you up in the best possible way.</p>
	<ul style="list-style-type: square;font-size: 1.4rem;">
		<li>Focus Mini — a mini-meditation to put you in the right frame of mind to focus</li>
		<li>Early Mornings — blow away the cobwebs and feel clearer with this single meditation</li>
		<li>Dealing With Distractions — recognize and let go distractions with this 10-day course</li>
		<li>Productivity — use this 10-day course to help you maintain focus when it’s needed most</li>
	</ul>
	<h3>TAKE A BREAK</h3>
	<p style="font-size: 1.4rem;">Sometimes, stepping away and taking a break can alleviate the pressure around trying to focus. Our focus should be effortless, and these exercises (and one animation) are designed to help you help yourself.</p>
	<ul style="list-style-type: square;font-size: 1.4rem;">
		<li>Sleeping — a single meditation to set up a quality night’s sleep ahead of studying</li>
		<li>Taking a Break — press pause and take a necessary breather — you’ve earned it!</li>
		<li>For the Weekend — put down the weight of studying with this day-to-day exercise</li>
		<li>Letting go of Effort — an animation to demonstrate the benefits of releasing all effort</li>
	</ul>
	<h3>PRE-EXAM</h3>
	<p style="font-size: 1.4rem;">You’ve done all the studying and prep. Now the big day has arrived. View these exercises almost as a mini pep-talk — something to get your head in the zone, and to calm the nerves, for when it matters.</p>
	<ul style="list-style-type: square;font-size: 1.4rem;">
		<li>Walking in the City — take a walk and be present with this on-the-go exercise</li>
		<li>Exam Prep – find that sweet spot of relaxed focus before the exam starts</li>
		<li>Breathe — keep breathing and stay calm with this mini-meditation</li>
		<li>Focus — harness that focus to ensure that clarity walks with you into the room</li>
	</ul>
	<h3>POST-EXAM</h3>
	<p style="font-size: 1.4rem;">It’s not always easy to unwind after a big exam/test so these exercises are designed to ensure you draw a line under your day and discover a deserved place of rest.</p>
	<ul style="list-style-type: square;font-size: 1.4rem;">
		<li>Restore — exhale, reset, and restore with this mini-meditation
		<li>Taking a Break — press pause and take a necessary breather — you’ve definitely earned it!</li>
		<li>End of Day — you’ve done all you can, so use this exercise to release the burden and let go</li>
		<li>Walking in Nature — nothing like nature (and a guided meditation) to bring perspective</li></li>
	</ul>
	<h3>LEARN SOMETHING NEW</h3>
	<p style="font-size: 1.4rem;">Learning a new skill or taking on a new professional role can be daunting, so these exercises are tailored to help you get your head around what’s required while keeping your feet on the ground.</p>
	<ul style="list-style-type: square;font-size: 1.4rem;">
		<li>Creativity — recognize and nurture your creativity with this inspiring 30-day course  </li>
		<li>Balance — a balanced mind is a less reactive mind, as this 10-day course will show you</li>
		<li>Navigating Change — peace of mind is still possible when everything is changing</li>
		<li>Learning a Skill — an animation to help you adapt in learning a new skill</li>
	</ul>
